public class InheritanceCar
{
    private long price;
    private String name;
    public void setName(String n)
    {
        name = n;
    }
    public void setPrice(long p)
    {
        price = p; 
    }
    public String getName()
    {
        return(name);
    }
    public long getPrice()
    {
        return(price);
    } 
}